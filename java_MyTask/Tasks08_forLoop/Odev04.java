package java_MyTask.Tasks08_forLoop;

public class Odev04 {

    public static void main(String[] args) {



//        1
//        2 6
//        3 7 10
//        4 8 11 13
//        5 9 12 14 15

        for (int i = 1; i < 6 ; i++) {
            for (int j = 0; j < i ; j++) {
                System.out.print(i+j);
            }
            System.out.println();

        }




    }
}
